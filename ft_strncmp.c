#include "libft.h"

int ft_strncmp(const char *str1, const char *str2, size_t n)
{
	unsigned int 	i;

	i = 0;
	while (str1[i] && i < n)
	{
		if (str1[i] > str2[i])
			return (1);
		if (str1[i] < str2[i])
			return (-1);
		i++;
	}
	return (0);
}